// And there are many more assignment operators like +=, -=, *=, /=, %=, **= etc. which can be used to perform various operations on the variables.

// Example:

let x = 10; // here we used our first assign operator which is "="
console.log("x = ", x)
x += 5;
// here we used our second assign operator which is "+=" and this operator called addition assignment operator
// The above code is equivalent to x = x + 5;
console.log("x = ", x); // Output: 15

x -= 6;
// here we used our third assign operator which is "-=" and this operator called subtraction assignment operator
// The above code is equivalent to x = x - 6;
console.log ("x = ", x); // Output: 9x **= 2;
// here we used our seventh assign operator which is "**=" and this operator called exponentiation assignment operator  

x *= 9;
// here we used our fourth assign operator which is "*=" and this operator called multiplication assignment operator
// The above code is equivalent to x = x * 9;
console.log("x = ", x); // Output: 81

x /= 3;
// here we used our fifth assign operator which is "/=" and this operator called division assignment operator
// The above code is equivalent to x = x / 3;
console.log("x = ", x); // Output: 27  

x %= 4;
// here we used our sixth assign operator which is "%=" and this operator called modulus assignment operator
// The above code is equivalent to x = x % 4;
console.log("x = ", x); // Output: 3        

x **= 2;
// here we used our seventh assign operator which is "**=" and this operator called exponentiation assignment operator  
// The above code is equivalent to x = x ** 2;
