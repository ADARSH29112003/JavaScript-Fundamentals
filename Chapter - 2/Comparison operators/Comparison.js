// There are 8 comparison operators in JavaScript:

// 1. equal to (==)
// 2. not equal to (!=)    
// 3. Equal to & type (===)    
// 4. Not equal to & type (!==)
// 5. Greater than (>)
// 6. Greater than or equal to (>=)
// 7. Less than (<)
// 8. Less than or equal to (<=)

// 1. Equal to (==)
let a = 5;
let b = 2;

console.log("a == b", a == b); // false
//In these console.log we are trying to say that the value of 'a' is equal to the value of 'b' 
//where 5 is not equal to 2, so the output will be false (these false and true are called boolean values, which are comes by default when we compare two values)

// 2. Not equal to (!=)
console.log("a != b", a != b); // true
//In this console.log we are trying to say that the value of 'a' is not equal to the value of 'b' 
//where 5 is not equal to 2, so the output will be true

// 3. Equal to & type (===)
//These signs are used to compare both value and type of the variable means first it will check the value and then it will check the type 'data type' of the variable
let c = "5";
console.log("a === c", a === c); //false
//In these statements we check first the value of 'a' and 'c' where equal which is 5 which is true but when we check or goes to check datatype of 'a' and 'c' then it is different which final show false and the output will be false.
