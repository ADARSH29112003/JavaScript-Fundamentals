let score = 20;

score += 10; // score = 20 + 10 = 30
console.log(score); // print 30
score -= 5; // score = 30 - 5 = 25
console.log(score); // print 25
score *= 2; // score = 25 * 2 = 50
console.log(score); // print 50
score /= 5;     // score = 50 / 5 = 10
console.log(score); // print 10
score %= 3; // score = 10 % 3 = 1
console.log(score); // score = 10 % 3 = 1, print 1