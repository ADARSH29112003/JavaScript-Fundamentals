// //Write a JavaScript code to do these operations:
// // do step by step:
// // 1. x++ ✅
// // 2. print✅
// // 3. ++x ✅
// // 4. print ✅
// // 5. x-- ✅
// // 6. print ✅
// // 7. --x ✅
// // 8. print ✅

// //  And Given value of x is 10

let x = 10; // x = 10
x++; // x = 10 + 1 = 11
console.log(x++); // print 11, then x = 11 + 1 = 12
++x; // x = 12 + 1 = 13
console.log(++x); // print 13, then x = 13 + 1 = 14
x--; // x = 14 - 1 = 13
console.log(x--);  // print 13, then x = 13 - 1 = 12
--x; // x = 12 - 1 = 11
console.log(--x);  // print 11, then x = 11 - 1 = 10
